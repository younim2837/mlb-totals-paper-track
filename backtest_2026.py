DATA_DIR = PROJECT_DIR / "data"

CLOSING_LINES_PATH = DATA_DIR / "kalshi_closing_lines_2026.tsv"
RESULTS_PATH = DATA_DIR / "backtest_2026_results.tsv"
SUMMARY_PATH = DATA_DIR / "backtest_2026_summary.json"


