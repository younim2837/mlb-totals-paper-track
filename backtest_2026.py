"""
2026 MLB Totals Backtest — Kalshi Closing Prices

Uses the last trade before first pitch (closing line) rather than the 10 AM PT
morning snapshot, giving a truer picture of the live-bet opportunity.

Prerequisites:
    python collect_kalshi_historical.py --mode closing --season 2026

Usage:
    python backtest_2026.py
    python backtest_2026.py --season 2026
    python backtest_2026.py --bankroll 10000

Output:
    data/backtest_2026_results.tsv   — one row per game
    data/backtest_2026_summary.json  — aggregate statistics
"""

